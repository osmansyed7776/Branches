public class SubtractionExample {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 10;
        int result = num1 - num2;
        System.out.println("Subtraction result: " + result); // Output: Subtraction result: 15

        double d1 = 50.5;
        double d2 = 15.2;
        double doubleResult = d1 - d2;
        System.out.println("Double subtraction result: " + doubleResult); // Output: Double subtraction result: 35.3
    }
}
